#include <unistd.h>

void	ft_put_char(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int s;

	s = 0;
	while (str[s] != '\0')
	{
		ft_put_char(str[s]);
		s++;
	}
}
